// Problem link: https://codeforces.com/group/MWSDmqGsZm/contest/219432/problem/N

#include <bits/stdc++.h>
using namespace std;
int main()
{
    char s;
    cin >> s;
    int n;
    cin >> n;
    for (int i = 0; i < n; i++)
    {
        int xi;
        cin >> xi;
        for (int j = 0; j < xi; j++)
        {
            cout << s;
        }
        cout << endl;
    }
    return 0;
}